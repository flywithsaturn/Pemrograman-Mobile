void main() {
  var names1 = <String>{};
  Set<String> names2 = {};

  // Menambahkan 1 elemen dengan .add()
  names1.add("Satrio Wisnu Adi Pratama");

  // Menambahkan banyak elemen sekaligus dengan .addAll()
  names2.addAll({"Satrio Wisnu Adi Pratama", "2341720219"});

  print(names1);
  print(names2);
}



